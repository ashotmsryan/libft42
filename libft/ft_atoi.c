#include <stdio.h>

int	ft_atoi(const char *str)
{
	int	i;
	int	j;
	int	k;

	j = 0;
	k = 1;
	i = 0;
	while (str[i] == '\t' || str[i] == ' '
		|| str[i] == '\r' || str[i] == '\f'
		|| str[i] == '\n' || str[i] == '\v')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			k = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		j = j * 10;
		j = j + (str[i] - '0');
		i++;
	}
	return (j * k);
}
